    public static int uniquePathsWithObstacles(int[][] obstacleGrid) {
        int row = obstacleGrid.length;
        if (row == 0) {
            return 0;
        }
        int col = obstacleGrid[0].length;
        if (col == 0){
            return 1;
        }
        if (obstacleGrid[0][0] == 1 || obstacleGrid[row - 1][col - 1] == 1) {
            return 0;
        }
        return onePointPath(obstacleGrid, row - 1, col - 1);
    }

    public static int onePointPath(int[][] grid, int point_i, int point_j) {
        if (point_i == 0 && point_j == 0) {
            return 1;
        }
        if (grid[point_i][point_j] == 1) {
            return 0;
        }
        if (point_i == 0) {
            return onePointPath(grid, point_i, point_j - 1);
        } else if (point_j == 0) {
            return onePointPath(grid, point_i - 1, point_j);
        }
        return onePointPath(grid, point_i - 1, point_j) + onePointPath(grid, point_i, point_j - 1);
    }
